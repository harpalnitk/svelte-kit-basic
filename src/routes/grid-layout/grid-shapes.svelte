    //We need to rectify the size of the image to make them fit together. 
    // Otherwise, they’re spaced far apart to the point where it 
    // doesn’t look like a grid of images.
    //ll you need to know is that the relationship between 
    // the radius of each circle is the square root of 2 (sqrt(2)).
    //  This is the value we need to increase the size of our images
    //   to fill the area. We will use 100%*sqrt(2) = 141% and be done!
  width: 141%; /* sqrt(2)*100% */
  aspect-ratio: 1;
  object-fit: cover;
  filter: grayscale(80%);
  //Note how I rotate them both by 45deg, but in the opposite direction.
  //Rotating the images in the negative direction prevents them from 
  //getting rotated with the grid so they stay straight.
  transform: scale(var(--_t,1)) rotate(-45deg);
  //Now, we apply a clip-path to clip a rhombus shape out of them.
  clip-path: polygon(50% 0,100% 50%,50% 100%,0 50%);
  cursor: pointer;
  transition: .2s linear;
}
